#include "ft_printf.h"

void	ft_d_i(int n, int *count)
{
	char	c;

	if (n == -2147483648)
	{
		write (1, "-2147483648", 11);
		*count = *count + 11;
		return ;
	}
	if (n < 0)
	{
		write (1, "-", 1);
		n *= -1;
		(*count)++;
	}
	if (n == 0)
	{
		write (1, "0", 1);
		(*count)++;
		return ;
	}
	if (n > 9)
		ft_d_i(n / 10, count);
	c = (n % 10) + '0';
	write (1, &c, 1);
	(*count)++;
}
